<div class="guide-section">
    <p>You need to retrieve API Path and API Token from your Bigcommerce admin account.</p>
        1. Please Login to your store's control panel.<br>
        2. Look at the top right conner, click on Setup &amp; Tools &gt; Advanced tools &gt; Legacy API Accounts.<br>
        3. Press "Create a Legacy API Account".<br>
        4. Enter Username. Copy API Path and API Token to the fields below.<br>
        5. Click Save.<br>
