<div class="form-group guide-section">
    <strong>In order to retrieve the API details and Store ID from your Ecwid store, perform the following steps: </strong>
    <ol style="list-style: square; padding-left: 20px;">
        <li>1. Log in to Ecwid admin Panel </li>
        <li>2. Step to Setting -> Apps -> Legacy API </li>
        <li>3. Copy Order API secret key, Product API secret key, and paste them in the fields below.</li>
        <li>4. Scroll to the bottom of the Legacy API page (or any other page in the admin panel), copy your Ecwid Store ID from the right corner, and paste it into the appropriate field.</li>        
    </ol>
</div>