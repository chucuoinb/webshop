    <strong>You need to retrieve API Key from your 3dcart admin account.</strong>
    <ol style="list-style: square; padding-left: 20px;">
        <li>Go to Modules.</li>
        <li>Look for the <b>SOAP API</b> module and click on its + Icon to expand the selection.</li>
        <li>Click on the <b>"Change Settings"</b> link</li>
        <li>Place a mark in the <b>"Enable API"</b> checkbox.</li>
        <li>Copy your <b>API key</b> and click on the <b>"Save"</b> button at the top right.</li>
