<div class="guide-section">
    <strong>In order to enable data migration to or from Shopify, you will need to provide an API Password for your store. So, here is a step-by-step tutorial that will help you to get this information by your own.</strong>
    <ol style="list-style: square; padding-left: 20px;">
        <li>At first, log in to your store's admin area, copy the admin url to use as "Cart Url" in the form below (example: https://xxxxxxx.myshopify.com).</li>
        <li>From admin area, go to <b>Apps Menu</b>.</li>
        <li>Than, press <b>Private Apps</b> button</li>
        <li>Click <b>Generate a Private App</b> button.</li>
        <li>Right after that, you will see your <b>API Password.</b> Copy and paste it into the corresponding field and proceed to the next step.</li>
    </ol>
</div>